def pow(x,n)
y=[[1,0],[0,1]]

	while n>0
		if n%2==1
			y[0][0]=y[0][0]*x[0][0]+y[0][1]*x[1][0]
			y[0][1]=y[0][0]*x[0][1]+y[0][1]*x[1][1]
			y[1][0]=y[1][0]*x[0][0]+y[1][1]*x[1][0]
			y[1][1]=y[1][0]*x[0][1]+y[1][1]*x[1][1]
		end 
		x[0][0]=x[0][0]*x[0][0]+x[0][1]*x[1][0]
		x[0][1]=x[0][0]*x[0][1]+x[0][1]*x[1][1]
		x[1][0]=x[1][0]*x[0][0]+x[1][1]*x[1][0]
		x[1][1]=x[1][0]*x[0][1]+x[1][1]*x[1][1]
		n/=2
	end
	return y[1][0]
end

